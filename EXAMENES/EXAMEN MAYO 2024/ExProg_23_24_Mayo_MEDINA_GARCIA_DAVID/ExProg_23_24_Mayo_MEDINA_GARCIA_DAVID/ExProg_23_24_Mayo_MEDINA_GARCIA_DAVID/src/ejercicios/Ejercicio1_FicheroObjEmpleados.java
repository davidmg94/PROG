    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed

        // comprueba que no haya ningun campo vacio o sin seleccionar
        if (algunoVacio(txtIdEmpleado, txtNombre, txtNroHijos, txtSalario) 
            || (!jRadioVendedor.isSelected() && !jRadioIngeniero.isSelected())) {
            JOptionPane.showMessageDialog(null, "Hay campos vacios o sin seleccionar.");
            return;
        }
        // comprueba que el idEmpleado cumpla con el patron establecido
        if (!Operaciones.correcto(txtIdEmpleado.getText(), patronEntero)) {
            JOptionPane.showMessageDialog(null, "El id debe ser de 5 digitos como maximo.");
            txtIdEmpleado.requestFocus();
            return;
        }
        // obtiene el id del formulario y comprueba si ya hay algun empleado con ese id
        int idEmpleado = Integer.parseInt(txtIdEmpleado.getText());
        if (Operaciones.existeEmpleado(idEmpleado)) {
            JOptionPane.showMessageDialog(null, "El empleado existe.");
            txtIdEmpleado.requestFocus();
            return;
        }

        // comprueba que todos los campos cumplan con los patrones requeridos
        if (!Operaciones.correcto(txtNombre.getText(), patronCadena)) {
            JOptionPane.showMessageDialog(null, "El nombre debe tener como maximo 50 caracteres.");
            txtNombre.requestFocus();
            return;

        } else if (!Operaciones.correcto(txtSalario.getText(), patronReal)) {
            JOptionPane.showMessageDialog(null, "El salario de ser un entero de maximo 6 digitos y maximo 2 decimales.");
            txtSalario.requestFocus();
            return;
        } else if (!Operaciones.correcto(txtNroHijos.getText(), patronHijos)) {
            JOptionPane.showMessageDialog(null, "El numero de hijos debe ser de 2 digitos como maximo.");
            txtNroHijos.requestFocus();
            return;
        }
        
        // obtiene los datos del formulario
        String nombre = txtNombre.getText();
        int nroHijos = Integer.parseInt(txtNroHijos.getText());
        float salario = Float.parseFloat(txtSalario.getText());
        
        String tipoEmpleado = "";
        if (jRadioIngeniero.isSelected()) {
            tipoEmpleado = "I";
        } else if (jRadioVendedor.isSelected()) {
            tipoEmpleado = "V";
        }
        int turno = -1;
        int elegirTurno = cboTipoPago.getSelectedIndex();
        switch (elegirTurno) {
            case 0:
                turno = 1;
                break;
            case 1:
                turno = 2;
                break;
            case 2:
                turno = 3;
                break;
            case 3:
                turno = 4;
                break;

        }

        // crea un objeto empleado con los datos del formulario
        Empleado e = new Empleado(idEmpleado, nombre, salario, nroHijos, tipoEmpleado, turno);
        // se guarda el empleado en el fichero
        Operaciones.grabarEmpleado(e);
        // limpia el formulario
        limpiarDatos();
        // pone el foco en el campo idEmpleado
        txtIdEmpleado.requestFocus();
    }//GEN-LAST:event_btnGrabarActionPerformed

