    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
      // comprueba que el idEmpleado no este en blanco
        if (txtIdEmpleado.getText().isBlank()) {
            JOptionPane.showMessageDialog(null, "ID no puede estar vacio.");
            return;
        }
        // comprueba que el idEmpleado cumpla con el patron
        if (!Operaciones.correcto(txtIdEmpleado.getText(), patronEntero)) {
            JOptionPane.showMessageDialog(null, "El id debe ser de 5 digitos como maximo.");
            txtIdEmpleado.requestFocus();
            return;
        }
        // obtiene el id del formulario y comprueba que exista el empleado
        int idEmpleado = Integer.parseInt(txtIdEmpleado.getText());
        if (!Operaciones.existeEmpleado(idEmpleado)) {
            JOptionPane.showMessageDialog(null, "Registro no existe.");
            txtIdEmpleado.requestFocus();
            return;
        }
        // almacena los datos del empleado encontrado
        Empleado emp = Operaciones.buscaEmpleado(idEmpleado);
        // se visualizan los datos del empleado en el formulario
        visualizarEmpleado(emp);
    }//GEN-LAST:event_btnBuscarActionPerformed
   // patrones que tienen que cumplir los campos del formulario
    Pattern patronCadena = Pattern.compile("[A-Za-z\\d\\s]{1,50}");
    Pattern patronEntero = Pattern.compile("[0-9]{1,5}");
    Pattern patronReal = Pattern.compile("\\d{1,6}(\\.\\d{1,2})?");
    Pattern patronHijos = Pattern.compile("\\d{1,2}");

    // comprueba que no haya ninguna campo vacio en el formulario
    private boolean algunoVacio(JTextField... campos) {
        for (JTextField campo : campos) {
            if (campo.getText().isBlank()) {
                txtIdEmpleado.requestFocus();
                return true;
            }
        }
        return false;
    }

    // limpia los datos del formulario
    private void limpiarDatos() {
        txtIdEmpleado.setText("");
        txtNombre.setText("");
        txtNroHijos.setText("");
        txtSalario.setText("");
        cboTipoPago.setSelectedIndex(0);
    }

    // muestra los datos del empleado en el formulario
    private void visualizarEmpleado(Empleado emp) {
        txtIdEmpleado.setText(String.valueOf(emp.getIdEmpleado()));
        txtNombre.setText(emp.getNombre());
        txtNroHijos.setText(String.valueOf(emp.getNroHijos()));
        txtSalario.setText(String.valueOf(emp.getSalario()));
        if (emp.getTipoEmpleado().equalsIgnoreCase("I")) {
            jRadioIngeniero.setSelected(true);
        } else {
            jRadioVendedor.setSelected(true);
        }
        cboTipoPago.setSelectedIndex(emp.getTurno());
    }

