    private void txtCodigoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtCodigoFocusLost
        if (txtCodigo.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "¡ERROR! El codigo no puede estar vacío.");
        } else {
            try {
                int num = Integer.parseInt(txtCodigo.getText());
                if (!Validacion.validacionNumeros(num)) {
                    JOptionPane.showMessageDialog(this, "¡ERROR! El codigo debe ser un número entero de 5 cifras como maximo.");
                }

            } catch (InputMismatchException | NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "¡ERROR! El codigo debe ser un número entero de 5 cifras como maximo.");
            }
        }

    }//GEN-LAST:event_txtCodigoFocusLost

    private void txtDescrpMuebleFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtDescrpMuebleFocusLost
        String descripcion = txtDescrpMueble.getText();
        if (!Validacion.validacionCadena(descripcion)) {
            JOptionPane.showMessageDialog(this, "¡ERROR! La descripción no puede estar vacía y no debe exceder los 15 caracteres.");
        }
    }//GEN-LAST:event_txtDescrpMuebleFocusLost

    private void txtPrecioUnitarioFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtPrecioUnitarioFocusLost
        if (txtPrecioUnitario.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "¡ERROR! El precio no puede estar vacío.");
        } else {
            try {
                double precio = Double.parseDouble(txtPrecioUnitario.getText());
                if (!Validacion.validacionPrecio(precio)) {
                    JOptionPane.showMessageDialog(null, "ERROR! El precio no puede estar vacío y debe ser mayor que 0 y tener dos decimales.");
                }
            } catch (InputMismatchException | NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "ERROR! El precio no puede estar vacío y debe ser mayor que 0 y tener dos decimales.");
            }
        }
    }//GEN-LAST:event_txtPrecioUnitarioFocusLost

