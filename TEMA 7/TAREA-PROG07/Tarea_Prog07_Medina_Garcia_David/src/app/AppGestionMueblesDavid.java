    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        try {
            // Validar si el campo txtCodigo está vacío
            if (txtCodigo.getText().isBlank()) {
                JOptionPane.showMessageDialog(this, "El codigo no puede estar vacío.");
                txtCodigo.requestFocus();
                return;
            }

            // Obtener el valor del campo txtCodigo
            int codigoMueble = Integer.parseInt(txtCodigo.getText());

            // Verificar si ya existe un registro con el mismo código
            if (OperacionesFichero.existeRegistro(codigoMueble)) {
                JOptionPane.showMessageDialog(this, "El codigo ya existe.");
                txtCodigo.requestFocus();
                return;
            }

            // almacenamos los campos en variables que se pasaran al objeto
            // convertimos a numero los campos necesarios
            String descripMueble = txtDescrpMueble.getText();
            double precioMueble = Double.parseDouble(txtPrecioUnitario.getText());
            int unidadesAlmacen = Integer.parseInt(txtUnidadesAlmacen.getText());
            int unidadesMinimas = Integer.parseInt(txtUnidadesMinimas.getText());
            // Validar el tipo de mueble seleccionado
            String elegirMueble = cmbTipoMueble.getSelectedItem().toString();
            TipoMueble tipoMueble = null;
            switch (elegirMueble) {
                case "Hogar":
                    tipoMueble = TipoMueble.H;
                    break;
                case "Despacho":
                    tipoMueble = TipoMueble.D;
                    break;
                case "Colegios":
                    tipoMueble = TipoMueble.C;
                    break;
                default:
                    JOptionPane.showMessageDialog(this, "Debe elegir un tipo de mueble.");
                    return;
            }

            // comprobar que las unidades de almacen sean superiores a las unidades minimas
            if (!Validacion.validacionUnidades(unidadesAlmacen, unidadesMinimas)) {
                JOptionPane.showMessageDialog(this, "Las unidades de almacen no pueden ser inferiores a las unidades minimas.");
                return;
            }

            // Validamos una vez mas los campos para asegurarnos que no se graben datos incorrectos en el archivo
            if (!Validacion.validacionCadena(descripMueble)
                    || !Validacion.validacionNumeros(codigoMueble)
                    || !Validacion.validacionPrecio(precioMueble)
                    || !Validacion.validacionNumeros(unidadesAlmacen)
                    || !Validacion.validacionNumeros(unidadesMinimas)) {
                JOptionPane.showMessageDialog(this, "Existen campos no validos en el formulario.");
                return;
            }
            // Obtener los valores de los otros campos y realizar la validación correspondiente
            // Crear un nuevo objeto Mueble con los valores obtenidos
            Mueble mueble = new Mueble();
            mueble.setIdMueble(codigoMueble);
            mueble.setDescripMueble(txtDescrpMueble.getText());
            mueble.setPrecioUnitario(precioMueble);
            mueble.setUnidadesAlmacen(unidadesAlmacen);
            mueble.setUnidadesMinimas(unidadesMinimas);
            mueble.setTipoMueble(tipoMueble);
            // Intentar grabar el mueble en el archivo
            if (OperacionesFichero.grabarRegistro(mueble)) {
                JOptionPane.showMessageDialog(this, "Mueble grabado exitosamente.");
            } else {
                JOptionPane.showMessageDialog(this, "Error al grabar el mueble.");
            }

            // Limpiar los campos y volver a enfocar txtCodigo
            limpiarDatos();
            txtCodigo.requestFocus();
        } catch (InputMismatchException | NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "¡ERROR! En los campos numericos debe ser un número entero.");
        }
    }//GEN-LAST:event_btnGrabarActionPerformed

