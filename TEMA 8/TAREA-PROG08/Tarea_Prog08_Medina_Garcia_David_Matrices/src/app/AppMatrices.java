    private void btnSumaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSumaActionPerformed
        try {
            // Obtenemos el nombre de la matriz seleccionada en el ComboBox
            String matriz = cmbMatriz.getSelectedItem().toString();
            // Obtenemos el nombre de la opción seleccionada en el ComboBox (fila o columna)
            String filaColumna = cmbFilaColumna.getSelectedItem().toString();
            // Obtenemos el índice de la opción seleccionada en el ComboBox (que va de 0 a 6)
            int nroFilaColumna = cmbNroFilaColumna.getSelectedIndex();

            // Verificamos si el número de fila o columna seleccionado es mayor que la dimensión de las matrices
            if (nroFilaColumna > Integer.parseInt(txtDimension.getText())) {
                JOptionPane.showMessageDialog(this, "El número elegido no puede ser mayor que la dimensión de las matrices");
            } else {
                if (matriz.equalsIgnoreCase("Matriz A")) {
                    // Verificamos si la matriz A está vacía
                    if (matrizA == null) {
                        JOptionPane.showMessageDialog(this, "La matriz A no tiene datos");
                        txtDimension.requestFocus();
                        return; // Salimos del método
                    }
                    // Verificamos si se seleccionó sumar una fila o una columna
                    if (filaColumna.equalsIgnoreCase("fila")) {
                        // Si se seleccionó sumar una fila, mostramos la suma en el campo de texto txtSuma
                        txtSuma.setText(Integer.toString(matrizA.sumaFila(nroFilaColumna)));
                    } else {
                        // Si se seleccionó sumar una columna, mostramos la suma en el campo de texto txtSuma
                        txtSuma.setText(Integer.toString(matrizA.sumaColumna(nroFilaColumna)));
                    }
                } else {
                    // Verificamos si la matriz B está vacía
                    if (matrizB == null) {
                        JOptionPane.showMessageDialog(this, "La matriz B no tiene datos");
                        txtDimension.requestFocus();
                        return; // Salimos del método
                    }
                    // Verificamos si se seleccionó sumar una fila o una columna
                    if (filaColumna.equalsIgnoreCase("fila")) {
                        // Si se seleccionó sumar una fila, mostramos la suma en el campo de texto txtSuma
                        txtSuma.setText(Integer.toString(matrizB.sumaFila(nroFilaColumna)));
                    } else {
                        // Si se seleccionó sumar una columna, mostramos la suma en el campo de texto txtSuma
                        txtSuma.setText(Integer.toString(matrizB.sumaColumna(nroFilaColumna)));
                    }
                }
            }
        } catch (HeadlessException | NumberFormatException e) {
            // Capturamos cualquier excepción de formato numérico o de entorno gráfico
            JOptionPane.showMessageDialog(this, "ERROR!! Dimensión no tiene valor");
            txtDimension.requestFocus();
        }

    }//GEN-LAST:event_btnSumaActionPerformed

