    // Método para llenar una tabla (JTable) con los datos de una matriz
    private void llenarjTable(Matriz m, JTable jt) {
        // Obtenemos la matriz de la instancia de la clase Matriz
        int[][] matriz = m.getMatriz();
        // Obtenemos la dimensión de la matriz
        int dimension = m.getDimension();
        // Creamos un nuevo modelo de tabla (DefaultTableModel) con la dimensión de la matriz
        DefaultTableModel model = new DefaultTableModel(dimension, dimension);

        // Iteramos sobre las filas de la matriz
        for (int i = 0; i < dimension; i++) {
            // Iteramos sobre las columnas de la matriz
            for (int j = 0; j < dimension; j++) {
                // Establecemos el valor de la celda en la posición (i, j) con el valor correspondiente de la matriz
                model.setValueAt(matriz[i][j], i, j);
            }
        }

        // Establecemos el modelo de tabla creado en la tabla proporcionada
        jt.setModel(model);
    }

    // limpiamos las tablas
    private void limpiarjTable(JTable jt) {
        DefaultTableModel model = (DefaultTableModel) jt.getModel();

        int rowCount = model.getRowCount();
        int columnCount = model.getColumnCount();

        for (int i = 0; i < rowCount; i++) {
            for (int j = 0; j < columnCount; j++) {
                model.setValueAt("", i, j);
            }
        }
    }

