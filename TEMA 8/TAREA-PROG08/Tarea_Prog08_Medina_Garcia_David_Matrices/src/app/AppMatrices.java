    private void btnSumaMatricesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSumaMatricesActionPerformed
        // Verificamos si alguna de las matrices es nula
        if (matrizB == null || matrizA == null) {
            JOptionPane.showMessageDialog(this, "Las matrices no tienen valores");
        } else if (matrizA.getDimension() != matrizB.getDimension()) { // Verificamos si las matrices tienen la misma dimensión
            JOptionPane.showMessageDialog(this, "Las matrices no tienen la misma dimensión");
        } else {
            // Si ambas matrices tienen valores y tienen la misma dimensión, realizamos la suma
            Matriz suma = matrizA.suma(matrizB);
            // Llenamos la tabla con el resultado de la suma
            llenarjTable(suma, jTableResultado);
        }

    }//GEN-LAST:event_btnSumaMatricesActionPerformed

